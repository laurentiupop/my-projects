#include "ft_list.h"

int	ft_list_size(t_list *begin_list)
{
	t_list	*tmp;
	int		c;

	c = 0;
	tmp = begin_list;
	while (tmp)
	{
		c++;
		tmp = tmp->next;
	}
	return (c);
}
