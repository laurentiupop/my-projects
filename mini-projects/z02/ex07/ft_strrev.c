char *ft_strrev(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	while (*(str + i) != '\0')
		i++;
	i--;
	j = 0;
	while (j <= i)
	{
		c = *(str + j);
		*(str + j) = *(str + i);
		*(str + i) = c;
		j++;
		i--;
	}
	return (str);
}
