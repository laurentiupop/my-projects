#include<unistd.h>

int ft_iterative_factorial(int nb)
{
	int i;
	int a;

	i = 0;
	a = 1;
	if (nb > 0 && nb <= 12)
	{
		while (i < nb)
		{
			a = (nb - i) * a;
			i++;
		}
		return (a);
	}
	else
		return (0);
}
