int ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	while (i * i <= nb && i * i > 0)
	{
		while (nb % i == 0)
		{
			i = 2;
			nb++;
		}
		i++;
	}
	return (nb);
}
