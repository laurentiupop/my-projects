int ft_atoi(char *str)
{
	int val;
	int i;
	int neg;

	val = 0;
	i = 0;
	neg = 1;
	while (*(str + i) == 32)
		i++;
	if (*(str + i) == 45)
	{
		neg = -1;
		i++;
	}
	else if (*(str + i) == 43)
		i++;
	while ((*(str + i) != '\0') || (val != 0))
	{
		if ((*(str + i) >= 48) && (*(str + i) <= 57))
			val = val * 10 + (*(str + i) - 48);
		else
			return (val * neg);
		i++;
	}
	return (0);
}
